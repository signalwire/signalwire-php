        $this->assertSame(200, $status);
        $decoded = json_decode($body, true);
        $this->assertSame('ready', $decoded['status']);
    }

    public function testHealthNoAuthRequired(): void
    {
        $svc = $this->makeService();
        [$status,,] = $svc->handleRequest('GET', '/health');
        $this->assertSame(200, $status);
    }

    // ------------------------------------------------------------------
    // HTTP Handling: Auth
    // ------------------------------------------------------------------

    public function testRootWithoutAuthReturns401(): void
    {
        $svc = $this->makeService();
        [$status, $headers,] = $svc->handleRequest('GET', '/');

        $this->assertSame(401, $status);
        $this->assertArrayHasKey('WWW-Authenticate', $headers);
    }

    public function testRootWithCorrectAuth(): void
    {
        $svc = $this->makeService();
        [$status, $headers, $body] = $svc->handleRequest('GET', '/', $this->authHeader());

        $this->assertSame(200, $status);
        $decoded = json_decode($body, true);
        $this->assertSame('1.0.0', $decoded['version']);
    }

    public function testWrongPasswordReturns401(): void
    {
        $svc = $this->makeService();
        [$status,,] = $svc->handleRequest('GET', '/', $this->authHeader('testuser', 'wrongpass'));
        $this->assertSame(401, $status);
    }

    public function testWrongUserReturns401(): void
    {
        $svc = $this->makeService();
        [$status,,] = $svc->handleRequest('GET', '/', $this->authHeader('wronguser', 'testpass'));
        $this->assertSame(401, $status);
    }

    public function testNoAuthHeaderReturns401(): void
    {
        $svc = $this->makeService();
        [$status,,] = $svc->handleRequest('GET', '/');
        $this->assertSame(401, $status);
    }

    public function testBadAuthSchemeReturns401(): void
    {
        $svc = $this->makeService();
        [$status,,] = $svc->handleRequest('GET', '/', ['Authorization' => 'Bearer token123']);
        $this->assertSame(401, $status);
    }

    // ------------------------------------------------------------------
    // HTTP Handling: Security Headers
    // ------------------------------------------------------------------

    public function testSecurityHeadersOnAuth(): void
    {
        $svc = $this->makeService();
        [$status, $headers,] = $svc->handleRequest('GET', '/', $this->authHeader());

        $this->assertSame(200, $status);
        $this->assertSame('nosniff', $headers['X-Content-Type-Options']);
        $this->assertSame('DENY', $headers['X-Frame-Options']);
        $this->assertSame('no-store', $headers['Cache-Control']);
    }

    public function testSecurityHeadersOn401(): void
    {
        $svc = $this->makeService();
        [, $headers,] = $svc->handleRequest('GET', '/');

        $this->assertSame('nosniff', $headers['X-Content-Type-Options']);
        $this->assertSame('DENY', $headers['X-Frame-Options']);
        $this->assertSame('no-store', $headers['Cache-Control']);
    }

    // ------------------------------------------------------------------
    // HTTP Handling: SWML / SWAIG / PostPrompt
    // ------------------------------------------------------------------

    public function testSwmlEndpointReturnsDocument(): void
    {
        $svc = $this->makeService();
        $svc->answer(['max_duration' => 3600])->hangup();

        [$status, , $body] = $svc->handleRequest('GET', '/', $this->authHeader());
        $this->assertSame(200, $status);

        $decoded = json_decode($body, true);
        $this->assertSame('1.0.0', $decoded['version']);
        $this->assertCount(2, $decoded['sections']['main']);
    }

    public function testSwaigEndpointAuth(): void
    {
        $svc = $this->makeService();
        [$status,,] = $svc->handleRequest('POST', '/swaig');
        $this->assertSame(401, $status);
    }

    public function testSwaigEndpointWithAuth(): void
    {
        // After the SWAIG lift, the endpoint validates the body. An empty
        // body with no `function` name now returns 400. The test exists to
        // prove the route is mounted under auth — 400 is the correct lifted
        // behavior; the stub-200 the old test asserted was a no-op handler.
        $svc = $this->makeService();
        [$status,,] = $svc->handleRequest('POST', '/swaig', $this->authHeader(), '{}');
        $this->assertSame(400, $status);
    }

    public function testPostPromptEndpointAuth(): void
    {
        $svc = $this->makeService();
        [$status,,] = $svc->handleRequest('POST', '/post_prompt');
        $this->assertSame(401, $status);
    }

    public function testPostPromptEndpointWithAuth(): void
    {
        $svc = $this->makeService();
        [$status,,] = $svc->handleRequest('POST', '/post_prompt', $this->authHeader(), '{}');
        $this->assertSame(200, $status);
    }

    // ------------------------------------------------------------------
    // HTTP Handling: Custom Route
