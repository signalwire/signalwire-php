    // ------------------------------------------------------------------
    // HTTP Handling: Body size limit
    // ------------------------------------------------------------------

    public function testBodySizeLimit(): void
    {
        $svc = $this->makeService();
        $hugeBody = str_repeat('x', 1_048_577); // 1MB + 1 byte
        [$status,,] = $svc->handleRequest('POST', '/', $this->authHeader(), $hugeBody);
        $this->assertSame(413, $status);
    }

    // ------------------------------------------------------------------
    // Routing Callbacks
    // ------------------------------------------------------------------

    public function testRoutingCallback(): void
    {
        $svc = $this->makeService();
        $called = false;
        $svc->registerRoutingCallback('/custom', function (?array $data, array $headers) use (&$called) {
            $called = true;
            return ['custom' => 'response'];
        });

        [$status, , $body] = $svc->handleRequest('POST', '/custom', $this->authHeader(), '{}');
        $this->assertTrue($called);
        $this->assertSame(200, $status);
        $decoded = json_decode($body, true);
        $this->assertSame('response', $decoded['custom']);
    }

    // ------------------------------------------------------------------
    // SIP Username Extraction
    // ------------------------------------------------------------------

    public function testExtractSipUsername(): void
    {
        $body = ['call' => ['to' => 'sip:agent1@example.com']];
        $this->assertSame('agent1', Service::extractSipUsername($body));
    }

    public function testExtractSipUsernameFromTo(): void
    {
        $body = ['to' => 'sip:myuser@host.com'];
        $this->assertSame('myuser', Service::extractSipUsername($body));
    }

    public function testExtractSipUsernameNull(): void
    {
        $this->assertNull(Service::extractSipUsername(null));
        $this->assertNull(Service::extractSipUsername([]));
    }

    public function testExtractSipUsernameRejectsInvalid(): void
    {
        // Invalid characters
        $body = ['to' => 'sip:user;drop@host.com'];
        $this->assertNull(Service::extractSipUsername($body));
    }

    public function testExtractSipUsernameTooLong(): void
    {
        $long = str_repeat('a', 65);
        $body = ['to' => "sip:{$long}@host.com"];
        $this->assertNull(Service::extractSipUsername($body));
    }

    public function testExtractSipUsernameValidChars(): void
    {
        $body = ['to' => 'sip:user.name-test_1@host.com'];
        $this->assertSame('user.name-test_1', Service::extractSipUsername($body));
    }

    // ------------------------------------------------------------------
    // Proxy URL Base
    // ------------------------------------------------------------------

    public function testProxyUrlFromEnv(): void
    {
        putenv('SWML_PROXY_URL_BASE=https://proxy.example.com');
        $svc = $this->makeService();
        $this->assertSame('https://proxy.example.com', $svc->getProxyUrlBase());
    }

    public function testProxyUrlFromForwardedHeaders(): void
    {
        $svc = $this->makeService();
        $headers = [
            'X-Forwarded-Proto' => 'https',
            'X-Forwarded-Host' => 'myapp.example.com',
        ];
        $this->assertSame('https://myapp.example.com', $svc->getProxyUrlBase($headers));
    }

    public function testProxyUrlFromOriginalUrl(): void
    {
        $svc = $this->makeService();
        $headers = ['X-Original-URL' => 'https://original.example.com/path'];
        $this->assertSame('https://original.example.com/path', $svc->getProxyUrlBase($headers));
    }

    public function testProxyUrlFallback(): void
    {
        $svc = $this->makeService(['host' => '0.0.0.0', 'port' => 3000]);
        $this->assertSame('http://0.0.0.0:3000', $svc->getProxyUrlBase());
    }

