<?php

declare(strict_types=1);

namespace SignalWire\Tests;

use PHPUnit\Framework\TestCase;
use SignalWire\SWML\Service;
use SignalWire\SWML\Schema;
use SignalWire\Logging\Logger;

class SWMLServiceTest extends TestCase
{
    protected function setUp(): void
    {
        Schema::reset();
        Logger::reset();
        putenv('SWML_BASIC_AUTH_USER');
        putenv('SWML_BASIC_AUTH_PASSWORD');
        putenv('SWML_PROXY_URL_BASE');
        putenv('PORT');
    }

    protected function tearDown(): void
    {
        Schema::reset();
        Logger::reset();
        putenv('SWML_BASIC_AUTH_USER');
        putenv('SWML_BASIC_AUTH_PASSWORD');
        putenv('SWML_PROXY_URL_BASE');
        putenv('PORT');
    }

    private function makeService(array $opts = []): Service
    {
        return new Service(
            name: $opts['name'] ?? 'test-service',
            route: $opts['route'] ?? '/',
            host: $opts['host'] ?? null,
            port: $opts['port'] ?? null,
            basicAuthUser: $opts['basic_auth_user'] ?? 'testuser',
            basicAuthPassword: $opts['basic_auth_password'] ?? 'testpass'
        );
    }

    private function authHeader(string $user = 'testuser', string $pass = 'testpass'): array
    {
        return ['Authorization' => 'Basic ' . base64_encode("{$user}:{$pass}")];
    }

    // ------------------------------------------------------------------
    // Construction
    // ------------------------------------------------------------------

    public function testConstruction(): void
    {
        $svc = $this->makeService();
        $this->assertSame('test-service', $svc->getName());
        $this->assertSame('/', $svc->getRoute());
        $this->assertSame('0.0.0.0', $svc->getHost());
        $this->assertSame(3000, $svc->getPort());
    }

    public function testCustomRoute(): void
    {
        $svc = $this->makeService(['route' => '/myagent']);
        $this->assertSame('/myagent', $svc->getRoute());
    }

    public function testRouteTrailingSlashStripped(): void
    {
        $svc = $this->makeService(['route' => '/myagent/']);
        $this->assertSame('/myagent', $svc->getRoute());
    }

    public function testPortFromEnv(): void
    {
        putenv('PORT=8080');
        $svc = new Service(
            name: 'test',
            basicAuthUser: 'u',
            basicAuthPassword: 'p'
        );
        $this->assertSame(8080, $svc->getPort());
    }

    public function testPortExplicitOverridesEnv(): void
    {
        putenv('PORT=8080');
        $svc = $this->makeService(['port' => 9090]);
        $this->assertSame(9090, $svc->getPort());
    }

    // ------------------------------------------------------------------
    // Auth
    // ------------------------------------------------------------------

    public function testExplicitAuth(): void
    {
        $svc = $this->makeService([
            'basic_auth_user' => 'myuser',
            'basic_auth_password' => 'mypass',
        ]);
        $this->assertSame(['myuser', 'mypass'], $svc->getBasicAuthCredentials());
    }

    public function testEnvAuth(): void
    {
        putenv('SWML_BASIC_AUTH_USER=envuser');
        putenv('SWML_BASIC_AUTH_PASSWORD=envpass');
        $svc = new Service(name: 'test');
        $this->assertSame(['envuser', 'envpass'], $svc->getBasicAuthCredentials());
    }

    public function testAutoGeneratedAuth(): void
    {
        $svc = new Service(name: 'test');
        [$user, $pass] = $svc->getBasicAuthCredentials();
        $this->assertNotEmpty($user);
        $this->assertNotEmpty($pass);
        $this->assertGreaterThan(8, strlen($user));
        $this->assertGreaterThan(8, strlen($pass));
    }

    public function testAutoGeneratedAuthIsDifferentEachTime(): void
    {
        $svc1 = new Service(name: 'test1');
        $svc2 = new Service(name: 'test2');
        [$user1,] = $svc1->getBasicAuthCredentials();
        [$user2,] = $svc2->getBasicAuthCredentials();
        $this->assertNotSame($user1, $user2);
    }

    public function testGetFullUrl(): void
    {
        $svc = $this->makeService();
        $this->assertSame('http://0.0.0.0:3000/', $svc->getFullUrl());
    }

    public function testGetFullUrlWithAuth(): void
    {
        $svc = $this->makeService([
            'basic_auth_user' => 'u',
            'basic_auth_password' => 'p',
        ]);
        $this->assertSame('http://u:p@0.0.0.0:3000/', $svc->getFullUrl(true));
    }

    public function testGetFullUrlWithRoute(): void
    {
        $svc = $this->makeService(['route' => '/agent']);
        $this->assertSame('http://0.0.0.0:3000/agent', $svc->getFullUrl());
    }

    // ------------------------------------------------------------------
    // Verb auto-vivification (__call)
    // ------------------------------------------------------------------

    public function testAnswerVerb(): void
    {
        $svc = $this->makeService();
        $svc->answer(['max_duration' => 3600]);

        $verbs = $svc->getDocument()->getVerbs('main');
        $this->assertCount(1, $verbs);
        $this->assertSame(['answer' => ['max_duration' => 3600]], $verbs[0]);
    }

    public function testHangupVerbNoArgs(): void
    {
        $svc = $this->makeService();
        $svc->hangup();

        $verbs = $svc->getDocument()->getVerbs('main');
        $this->assertCount(1, $verbs);
        $this->assertSame(['hangup' => []], $verbs[0]);
    }

    public function testSleepVerbInteger(): void
    {
        $svc = $this->makeService();
        $svc->sleep(2000);

        $verbs = $svc->getDocument()->getVerbs('main');
        $this->assertSame(['sleep' => 2000], $verbs[0]);
    }

    public function testSleepVerbWithSection(): void
    {
        $svc = $this->makeService();
        $svc->getDocument()->addSection('custom');
        $svc->sleep('custom', 1000);

        $verbs = $svc->getDocument()->getVerbs('custom');
        $this->assertSame(['sleep' => 1000], $verbs[0]);
    }

    public function testVerbWithSection(): void
    {
        $svc = $this->makeService();
        $svc->getDocument()->addSection('custom');
        $svc->answer('custom', ['max_duration' => 7200]);

        $verbs = $svc->getDocument()->getVerbs('custom');
        $this->assertSame(['answer' => ['max_duration' => 7200]], $verbs[0]);
    }

    public function testVerbChaining(): void
    {
        $svc = $this->makeService();
        $result = $svc->answer(['max_duration' => 3600]);
        $this->assertSame($svc, $result);
    }

    public function testMultipleVerbs(): void
    {
        $svc = $this->makeService();
        $svc->answer(['max_duration' => 3600])
            ->sleep(1000)
            ->hangup();

        $verbs = $svc->getDocument()->getVerbs('main');
        $this->assertCount(3, $verbs);
    }

    public function testAllSchemaVerbsCallable(): void
    {
        $svc = $this->makeService();
        $schema = Schema::instance();
        $names = $schema->getVerbNames();

        foreach ($names as $verb) {
            if ($verb === 'sleep') {
                $svc->sleep(1000);
            } else {
                $svc->$verb([]);
            }
        }

        $verbs = $svc->getDocument()->getVerbs('main');
        $this->assertCount(count($names), $verbs);
    }

    public function testUnknownMethodThrows(): void
    {
        $svc = $this->makeService();
        $this->expectException(\BadMethodCallException::class);
        $svc->nonexistentVerb();
    }

    // ------------------------------------------------------------------
    // HTTP Handling: Health/Ready
    // ------------------------------------------------------------------

    public function testHealthEndpoint(): void
    {
        $svc = $this->makeService();
        [$status, $headers, $body] = $svc->handleRequest('GET', '/health');

        $this->assertSame(200, $status);
        $this->assertSame('application/json', $headers['Content-Type']);
        $decoded = json_decode($body, true);
        $this->assertSame('healthy', $decoded['status']);
    }

    public function testReadyEndpoint(): void
    {
        $svc = $this->makeService();
